func main() {
	// Все анализаторы из passes
	analyzersPasses := []*analysis.Analyzer{
		asmdecl.Analyzer,
		assign.Analyzer,
		atomic.Analyzer,
		bools.Analyzer,
		buildtag.Analyzer,
		cgocall.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		ctrlflow.Analyzer,
		deepequalerrors.Analyzer,
		errorsas.Analyzer,
		httpresponse.Analyzer,
		ifaceassert.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilness.Analyzer,
		nilfunc.Analyzer,
		printf.Analyzer,
		shift.Analyzer,
		sigchanyzer.Analyzer,
		sortslice.Analyzer,
		stdmethods.Analyzer,
		stringintconv.Analyzer,
		structtag.Analyzer,
		testinggoroutine.Analyzer,
		tests.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
	}

	IDSAGroup := "SA"
	var analyzersSAGroup []*analysis.Analyzer

	for _, v := range staticcheck.Analyzers {
		if strings.HasPrefix(v.Analyzer.Name, IDSAGroup) {
			analyzersSAGroup = append(analyzersSAGroup, v.Analyzer)
		}
	}

	analyzersAll := append(analyzersPasses, analyzersSAGroup...)

	multichecker.Main(
		analyzersAll...,
	)
}
